interface Square {
  type: number;
  className: string;
}

const Pac: React.FC = () => {
  const [score, setScore] = useState(0);
  const [pacmanCurrentIndex, setPacmanCurrentIndex] = useState(490);
  const [grid, setGrid] = useState<Square[]>([]);

  useEffect(() => {
    // Initialize the grid based on the layout
    const initialGrid: Square[] = layout.map(type => ({
      type,
      className: typeToClass(type),
    }));
    setGrid(initialGrid);
  }, []);

  const typeToClass = useCallback((type: number): string => {
    switch(type) {
      case 0: return 'pac-dot';
      case 1: return 'wall';
      // Define additional cases as needed
      default: return '';
    }
  }, []);

  const movePacman = useCallback((e: KeyboardEvent) => {
    const allowedKeys = ['ArrowUp', 'ArrowDown', 'ArrowLeft', 'ArrowRight'];
    if (!allowedKeys.includes(e.key)) return;

    let newIndex = pacmanCurrentIndex;
    switch (e.key) {
      case 'ArrowLeft':
        newIndex = pacmanCurrentIndex - 1;
        break;
      case 'ArrowUp':
        newIndex = pacmanCurrentIndex - width;
        break;
      case 'ArrowRight':
        newIndex = pacmanCurrentIndex + 1;
        break;
      case 'ArrowDown':
        newIndex = pacmanCurrentIndex + width;
        break;
    }

    // Prevent Pac-Man from moving into walls
    if (!grid[newIndex] || grid[newIndex].className.includes('wall')) return;

    // Update grid to reflect Pac-Man's new position
    setGrid(currentGrid => {
      const newGrid = [...currentGrid];
      newGrid[pacmanCurrentIndex] = {...newGrid[pacmanCurrentIndex], className: typeToClass(newGrid[pacmanCurrentIndex].type)};
      newGrid[newIndex] = {...newGrid[newIndex], className: 'pac-man'};
      return newGrid;
    });
    
    setPacmanCurrentIndex(newIndex);
  }, [pacmanCurrentIndex, grid, typeToClass]);

  useEffect(() => {
    document.addEventListener('keyup', movePacman);
    return () => document.removeEventListener('keyup', movePacman);
  }, [movePacman]);

  return (
    <div>
      <div className="grid">
        {grid.map((square, index) => (
          <div key={index} className={`square ${square.className} ${index === pacmanCurrentIndex ? 'pac-man' : ''}`}></div>
        ))}
      </div>
      <h3>Score: <span id="score">{score}</span></h3>
    </div>
  );
};

export default Pac;
